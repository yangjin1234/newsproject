package pojo;

public class Type {

	private int tid;
	private int tid_nid_key;
	private String tname;
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public int getTid_nid_key() {
		return tid_nid_key;
	}
	public void setTid_nid_key(int tid_nid_key) {
		this.tid_nid_key = tid_nid_key;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
}
