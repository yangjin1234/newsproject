package pojo.impl;

import java.io.Serializable;

public class TimeTypec implements Serializable{

	private String tcc;
	private int number;
	public String getTcc() {
		return tcc;
	}
	public void setTcc(String tcc) {
		this.tcc = tcc;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
}
